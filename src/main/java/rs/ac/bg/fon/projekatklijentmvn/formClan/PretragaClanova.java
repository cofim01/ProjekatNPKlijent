    private void pripremiFormu() {
        postaviTabeluClanovi();
    }

    private void postaviTabeluClanovi() {
        try {
            ModelTabeleClanovi model=new ModelTabeleClanovi();
            Clan c=new Clan();
            ArrayList<Clan> clanovi=KlijentKontroler.getInstanca().vratiSveClanove(c);
            model.setSviClanovi(clanovi);
            tabelaClanovi.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
