    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        // TODO add your handling code here:
//        if(txtIme.getText().isEmpty() || txtPrezime.getText().isEmpty()){
//            JOptionPane.showMessageDialog(this, "Sva polja su obavezna!");
//            return;
//        }
        String ime=txtIme.getText();
        String prezime=txtPrezime.getText();
        Clan c=new Clan();
        c.setIme(ime);
        c.setPrezime(prezime);
        try {
            ArrayList<Clan> clanovi=KlijentKontroler.getInstanca().vratiClanovePoUslovu(c);
            ModelTabeleClanovi model=(ModelTabeleClanovi) tabelaClanovi.getModel();
            model.setSviClanovi(clanovi);
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        // TODO add your handling code here:
        try {
            int index=tabelaClanovi.getSelectedRow();
            ModelTabeleClanovi model=(ModelTabeleClanovi) tabelaClanovi.getModel();
            Clan clan=model.vratiClana(index);
            DetaljiClan detaljiFrm=new DetaljiClan((Frame) this.getParent(), true, clan,tabelaClanovi);
            JOptionPane.showMessageDialog(this, "Sistem je učitao člana biblioteke.");
            detaljiFrm.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita člana biblioteke.");
        }
    }//GEN-LAST:event_btnDetaljiActionPerformed

