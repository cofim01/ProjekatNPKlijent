    private void pripremiFormu() {
        try {
            ModelTabeleKnjige model=new ModelTabeleKnjige();
            Knjiga k=new Knjiga();
            KnjigaAutor ka=new KnjigaAutor();
            ArrayList<Knjiga> knjige=KlijentKontroler.getInstanca().vratiSveKnjige(k);
            ArrayList<KnjigaAutor> knjigaAutor=KlijentKontroler.getInstanca().vratiSveAutoreKnjige(ka);
            model.setKnjige(knjige);
            model.setKnjigaAutor(knjigaAutor);
            tabelaKnjige.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
    }

    

