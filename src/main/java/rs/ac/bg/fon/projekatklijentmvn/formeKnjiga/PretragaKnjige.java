    private void txtNazivKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNazivKeyReleased
        // TODO add your handling code here:
        String parametar=txtNaziv.getText();
        ModelTabeleKnjige model=(ModelTabeleKnjige) tabelaKnjige.getModel();
        try {
            Knjiga k=new Knjiga();
            k.setNaziv(parametar);
            ArrayList<Knjiga> lista=KlijentKontroler.getInstanca().vratiKnjigevePoUslovu(k);
            model.setKnjige(lista);
            tabelaKnjige.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_txtNazivKeyReleased

    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        // TODO add your handling code here:
        try {
            int index = tabelaKnjige.getSelectedRow();
            ModelTabeleKnjige model = (ModelTabeleKnjige) tabelaKnjige.getModel();
            Knjiga k = model.vratiKnjigu(index);
            String autor = model.vratiAutoreKnjige(k);
            DetaljiKnjiga detalji = new DetaljiKnjiga((Frame) this.getParent(), true, k, autor,tabelaKnjige);
            JOptionPane.showMessageDialog(this, "Sistem je učitao knjigu.");
            detalji.setVisible(true);
        } catch (Exception e) {
            //e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita knjigu.");
        }

    }//GEN-LAST:event_btnDetaljiActionPerformed

