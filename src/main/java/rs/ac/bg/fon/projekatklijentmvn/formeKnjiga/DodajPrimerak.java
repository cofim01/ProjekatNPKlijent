    private void pripremiFormu() {
        ModelTabelePrimerci model=new ModelTabelePrimerci();
        tabelaNoviPrimerci.setModel(model);
        btnDodajPrimerak.setEnabled(false);
    }

    

    private void postaviPrimerkeKnjige() {
        try {
            PrimerakKnjige pk=new PrimerakKnjige();
            pk.setKnjiga(k);
            ArrayList<PrimerakKnjige> lista=KlijentKontroler.getInstanca().vratiSvePrimerke(pk);
            ModelTabelePrimerci model=(ModelTabelePrimerci) tabelaNoviPrimerci.getModel();
            model.setListaPrimeraka(lista);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
