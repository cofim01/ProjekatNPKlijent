    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Knjiga knjiga=new Knjiga();
        IzaberiKnjigu FrmIzaberi=new IzaberiKnjigu((Frame) this.getParent(), true, knjiga);
        FrmIzaberi.setVisible(true);
        FrmIzaberi.setLocationRelativeTo(null);
        if(knjiga.getNaziv()!=null){
            lblKnjiga.setText(knjiga.getNaziv()+"");
            k=knjiga;
            btnDodajPrimerak.setEnabled(true);
            postaviPrimerkeKnjige();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnDodajPrimerakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajPrimerakActionPerformed
        // TODO add your handling code here:
        PrimerakKnjige pk=new PrimerakKnjige();
        NoviPrimerak primerakFrm=new NoviPrimerak((Frame) this.getParent(), true, pk);
        primerakFrm.setVisible(true);
        if(pk.getIzdavac()!=null){
            try {
                pk.setKnjiga(k);
                int p=KlijentKontroler.getInstanca().dodajPrimerakKnjige(pk);
                pk.setPrimerakId(p);
                JOptionPane.showMessageDialog(this, "Uspe≈°no ste dodali primerak knjige");
                ModelTabelePrimerci model=(ModelTabelePrimerci) tabelaNoviPrimerci.getModel();
                model.dodajPrimerak(pk);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
 
        }
        
    }//GEN-LAST:event_btnDodajPrimerakActionPerformed

    

