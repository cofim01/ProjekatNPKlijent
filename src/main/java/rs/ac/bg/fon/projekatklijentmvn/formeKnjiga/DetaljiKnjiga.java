    private void btnUcitajPrimerakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUcitajPrimerakActionPerformed
        // TODO add your handling code here:
        ModelTabelePrimerci model=(ModelTabelePrimerci) tabelaPrimerci.getModel();
        int index=tabelaPrimerci.getSelectedRow();
        if(index>=0){
        PrimerakKnjige pk=model.vratiPrimerakIndex(index);
        DetaljiPrimerak detaljiFrm=new DetaljiPrimerak((JFrame)this.getParent(), true,pk,this);
        detaljiFrm.setVisible(true);
        detaljiFrm.setLocationRelativeTo(null);
        osveziTabelu();
        }else{
            JOptionPane.showMessageDialog(this, "Niste selktovali željenu knjigu!");
        }
    }//GEN-LAST:event_btnUcitajPrimerakActionPerformed

    

