    private void pripremiFormu() {
        pripremiKnjigu();
        pripremiTabelu();
        
        
        
    }
    
    private void pripremiKnjigu(){
        txtIdKnjige.setText(knjiga.getKnjigaId()+"");
        txtAutor.setText(autor);
        txtZanr.setText(knjiga.getZanr().getNaziv());
        txtNaziv.setText(knjiga.getNaziv());
        txtKreirao.setText(knjiga.getKorisnik().getIme()+" "+knjiga.getKorisnik().getPrezime());
        txtAutor.setText(autor);
    }

    private void pripremiTabelu() {
        ModelTabelePrimerci model=new ModelTabelePrimerci(knjiga);
        
        model.refresh(knjiga);
        tabelaPrimerci.setModel(model);
        
        
    }

    void osveziTabelu() {
        ModelTabelePrimerci model=(ModelTabelePrimerci) tabelaPrimerci.getModel();
        model.refresh(knjiga);
        tabelaPrimerci.setModel(model);
    }
